def _generate_value(rng: random.Random) -> str:
    """Generate a distinctive synthetic value that's easy to match."""
    style = rng.choice(["code", "decimal", "integer"])
    if style == "code":
        letters = "".join(rng.choices("ABCDEFGHJKLMNPQRSTUVWXYZ", k=3))
        nums = rng.randint(1000, 9999)
        return f"{letters}-{nums}"
    elif style == "decimal":
        return f"{rng.uniform(10, 999):.2f}"
    else:
        return str(rng.randint(100, 99999))


def _generate_date(rng: random.Random) -> str:
    month = rng.choice(["January", "February", "March", "April", "May", "June",
                         "July", "August", "September", "October", "November", "December"])
    day = rng.randint(1, 28)
    year = rng.randint(2018, 2025)
    return f"{month} {day}, {year}"


def build_niah_item(
    rng: random.Random,
    target_context_words: int,
    needle_position: str,  # "early", "middle", "late"
) -> dict:
    """Build one NIAH benchmark item."""
    # Pick needle template and generate unique values
    template_idx = rng.randint(0, len(NEEDLE_TEMPLATES) - 1)
    needle_tmpl, question, answer_tmpl = NEEDLE_TEMPLATES[template_idx]
    value = _generate_value(rng)
    date = _generate_date(rng)
    needle_text = needle_tmpl.format(value=value, date=date)
    answer = answer_tmpl.format(value=value)

    # Build distractor context to target length
    # Each distractor paragraph is ~80-120 words
    used_distractors = list(DISTRACTORS)
    rng.shuffle(used_distractors)

    paragraphs = []
    word_count = 0
    for d in used_distractors:
        paragraphs.append(d)
        word_count += len(d.split())
        if word_count >= target_context_words:
            break

    # If we need more words, repeat distractors with slight variation
    while word_count < target_context_words:
        d = rng.choice(DISTRACTORS)
        paragraphs.append(d)
        word_count += len(d.split())

    # Insert needle at the specified position
    n_paras = len(paragraphs)
    if needle_position == "early":
        insert_idx = max(1, n_paras // 6)
    elif needle_position == "middle":
        insert_idx = n_paras // 2
    else:  # late
        insert_idx = max(1, 5 * n_paras // 6)

    paragraphs.insert(insert_idx, needle_text)
    context = "\n\n".join(paragraphs)

    return {
        "context": context,
        "question": question,
        "answer": answer,
        "meta": {
            "benchmark": "niah_synthetic",
            "needle_position": needle_position,
            "target_context_words": target_context_words,
            "actual_context_words": len(context.split()),
            "needle_template_idx": template_idx,
            "value": value,
        }
    }


def main():
    parser = argparse.ArgumentParser(description="Generate synthetic NIAH dataset")
    parser.add_argument("--output", default="data/niah-benchmark.jsonl")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--items-per-config", type=int, default=7,
                        help="Items per (length × position) config")
    args = parser.parse_args()

    rng = random.Random(args.seed)

    # Configuration grid: 3 lengths × 3 needle positions × N items each
    configs = []
    for target_words in [800, 1500, 3000]:  # ~1K, ~2K, ~4K tokens
        for position in ["early", "middle", "late"]:
            for _ in range(args.items_per_config):
                configs.append((target_words, position))

    items = []
    for target_words, position in configs:
        item = build_niah_item(rng, target_words, position)
        items.append(item)

    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        for item in items:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    print(f"Generated {len(items)} NIAH items → {out_path}")
    # Summary stats
    for tw in [800, 1500, 3000]:
        subset = [i for i in items if i["meta"]["target_context_words"] == tw]
        avg_words = sum(i["meta"]["actual_context_words"] for i in subset) / len(subset)
        print(f"  target={tw}w: {len(subset)} items, avg actual={avg_words:.0f} words")


if __name__ == "__main__":
    main()
